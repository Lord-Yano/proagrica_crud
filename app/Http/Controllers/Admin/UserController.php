    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        // Destroy user with ID
        User::destroy($id);

        // redirect user after deletion
        return redirect(route('admin.users.index'));
